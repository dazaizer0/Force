using System;

namespace Force.Source.Force2D.FMath
{
    internal class vector2int
    {
        public int x;
        public int y;

        public vector2int(int xv, int yv)
        {
            x = xv;
            y = yv;
        }
    }

    internal class vector2float
    {
        public float x;
        public float y;

        public vector2float(float xv, float yv)
        {
            x = xv;
            y = yv;
        }
    }
}
